package com.cse110.personalbest;

public class Friend {
    public String nickname;
    public String email;

    public Friend() {

    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEmail() {
        return email;
    }

    public String getNickname() {
        return nickname;
    }
}
